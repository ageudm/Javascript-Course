let x = 23;
let y = 10;
let texto = 'texto informativo'

// Adição numerica
console.log(x + 50);

// Concatenação de string
console.log(texto + ' de angola')

// Concatenação de strign com um inteiro
console.log(texto + ' de  ' + 2023);

// Adição com a propriedade boolean
console.log(x + true);

// Atribução de adição
console.log(x += 10);
console.log(texto += ' magi...')

// Operador de atribuição
console.log(x = 23);
console.log(texto = 'Miguel')

// Operador Bit a Bit (&)
// NÃO bit a bit (~)
// OU bit a bit (|)
// Atribuição OR bit a bit (|=)
// XOR bit a bit (^)
// Atribuição XOR bit a bit (^=)
console.log(x & y);  // Assunto para estudo aprofundado

// Operador condicional (ternário)...

// Diminuir (--)
console.log(x--);
console.log(--x);
console.log(y = x--)

// Incremento (++)
console.log(x++);
console.log(++x);
console.log(y = x++)


















